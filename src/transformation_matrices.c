#include "fdf.h"

void	make_x_matrix(float (*mat)[4][4], float a)
{
	(*mat)[0][0] = 1;
	(*mat)[0][1] = 0;
	(*mat)[0][2] = 0;
	(*mat)[0][3] = 0;
	(*mat)[1][0] = 0;
	(*mat)[1][1] = cos(a);
	(*mat)[1][2] = sin(a);
	(*mat)[1][3] = 0;
	(*mat)[2][0] = 0;
	(*mat)[2][1] = -sin(a);
	(*mat)[2][2] = cos(a);
	(*mat)[2][3] = 0;
	(*mat)[3][0] = 0;
	(*mat)[3][1] = 0;
	(*mat)[3][2] = 0;
	(*mat)[3][3] = 1;
}

void	make_y_matrix(float (*mat)[4][4], float a)
{
	(*mat)[0][0] = cos(a);
	(*mat)[0][1] = 0;
	(*mat)[0][2] = -sin(a);
	(*mat)[0][3] = 0;
	(*mat)[1][0] = 0;
	(*mat)[1][1] = 1;
	(*mat)[1][2] = 0;
	(*mat)[1][3] = 0;
	(*mat)[2][0] = sin(a);
	(*mat)[2][1] = 0;
	(*mat)[2][2] = cos(a);
	(*mat)[2][3] = 0;
	(*mat)[3][0] = 0;
	(*mat)[3][1] = 0;
	(*mat)[3][2] = 0;
	(*mat)[3][3] = 1;
}

void	make_z_matrix(float (*mat)[4][4], float a)
{
	(*mat)[0][0] = cos(a);
	(*mat)[0][1] = sin(a);
	(*mat)[0][2] = 0;
	(*mat)[0][3] = 0;
	(*mat)[1][0] = -sin(a);
	(*mat)[1][1] = cos(a);
	(*mat)[1][2] = 0;
	(*mat)[1][3] = 0;
	(*mat)[2][0] = 0;
	(*mat)[2][1] = 0;
	(*mat)[2][2] = 1;
	(*mat)[2][3] = 0;
	(*mat)[3][0] = 0;
	(*mat)[3][1] = 0;
	(*mat)[3][2] = 0;
	(*mat)[3][3] = 1;
}

void	make_translate_matrix(float (*mat)[4][4], float x, float y)
{
	(*mat)[0][0] = 1;
	(*mat)[0][1] = 0;
	(*mat)[0][2] = 0;
	(*mat)[0][3] = x;
	(*mat)[1][0] = 0;
	(*mat)[1][1] = 1;
	(*mat)[1][2] = 0;
	(*mat)[1][3] = y;
	(*mat)[2][0] = 0;
	(*mat)[2][1] = 0;
	(*mat)[2][2] = 1;
	(*mat)[2][3] = 0;
	(*mat)[3][0] = 0;
	(*mat)[3][1] = 0;
	(*mat)[3][2] = 0;
	(*mat)[3][3] = 0;
}

void	make_scale_matrix(float (*mat)[4][4], float x, float y, float z)
{
	(*mat)[0][0] = x;
	(*mat)[0][1] = 0;
	(*mat)[0][2] = 0;
	(*mat)[0][3] = 0;
	(*mat)[1][0] = 0;
	(*mat)[1][1] = y;
	(*mat)[1][2] = 0;
	(*mat)[1][3] = 0;
	(*mat)[2][0] = 0;
	(*mat)[2][1] = 0;
	(*mat)[2][2] = z;
	(*mat)[2][3] = 0;
	(*mat)[3][0] = 0;
	(*mat)[3][1] = 0;
	(*mat)[3][2] = 0;
	(*mat)[3][3] = 1;
}
